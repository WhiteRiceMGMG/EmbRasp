/*ハードウェアの動作は．起動先アドレスを決定．(リセットベクタの読み込み)
アドレス0x10000000(XIP FLASHの先頭)にアクセス
その先にあるベクタテーブルからリセットハンドラのアドレスを取得
取得したリセットハンドラアドレスにジャンプ
ソフトウェアで実行すること
リセットハンドラ．この関数は最初に実行されるコードであり，
通常はstartup.Sというアセンブリファイルなどで以下のような初期化を行う
・スタックポインタの初期化
・.data .bssの初期化(RAMへのコピー，ゼロ初期化)
・mainへジャンプ
Reset_Handler:
    ldr sp, =_stack_top     ; スタックポインタ初期化
    bl  SystemInit          ; クロック初期化など（C関数呼び出し）
    bl  main                ; Cのmain関数へジャンプ
・ベクタテーブル(boot2)はアドレス0x10000000に書き込まれる割り込み例外ハンドラのアドレス一覧
const void* vector_table[] __attribute__((section(".vectors"))) = {
    &_stack_top,        // スタック初期値
    Reset_Handler,      // リセットハンドラ（＝起動時に最初に呼ばれる）
    ...
};
このベクタテーブルが最初の4バイト，8バイト…と順番に並んでおり，
リセット時は2番目の値（PC初期値）にジャンプされる．

・ハードがやること→0x10000004の値(boot2,リセットベクタ)を読み，PCに設定
・ソフトがやること→リセットハンドラを書く(アセンブリ・C) + ベクタテーブル配置
・最小構成の責任であり，startup.S vector.c link.ldは作成する．

*/
